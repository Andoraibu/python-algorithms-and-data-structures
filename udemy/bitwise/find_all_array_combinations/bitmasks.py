'''
есть массив [1,2,3]
найти все его подмножества, а именно:
битмаски
[] <-- 000
[1] <-- 100
[2] <-- 010
[3] <-- 001
[1,2] <-- 110
[2,3] <-- 011
[1,3] <-- 101
[1,2,3] <-- 111
0 -> 2^n - 1
'''

arr = [1,2,3,4]
n = len(arr)
for mask in range(0, 1 << n):   # 1 << n = 2^n
    x = []
    # будем идти по каждому биту
    for i in range(0, n):
        # является ли наш бит единицей
        if mask & (1 << i):
            x.append(arr[i])
    print(f'{x}')
